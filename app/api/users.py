# GET     /users/{user_id}/tasks              → Get all tasks of a user
# POST    /users/{user_id}/tasks              → Create task for a user
# GET     /users/{user_id}/tasks/{task_id}    → Get user's specific task
# PUT     /users/{user_id}/tasks/{task_id}    → Update user's task
# DELETE  /users/{user_id}/tasks/{task_id}    → Delete user's task

